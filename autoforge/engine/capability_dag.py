"""CapabilityDAG — Self-Growing Universal Knowledge Graph.

A domain-agnostic, community-scalable directed acyclic graph of verified
capabilities. Every piece of knowledge AutoForge accumulates — whether it's
a mathematical proof, a code pattern, an architectural decision, a UI
convention, or a debugging strategy — lives as a node in this graph.

Core principles:
  1. **Content-addressed**: Nodes are identified by content hash,
     enabling deduplication across users/projects/communities.
  2. **Domain-agnostic**: Math proofs, code snippets, design patterns,
     and workflow strategies share the same graph structure.
  3. **Verification-polymorphic**: Each domain defines its own verification
     oracle — Lean type-checker for math, test suites for code, user
     ratings for design, LLM evaluation for everything else.
  4. **Self-growing**: The graph autonomously generates candidate nodes
     (via STP-style mutation/composition) and verifies them.
  5. **Community-mergeable**: Multiple DAG instances can merge, with
     conflict resolution based on verification confidence.
  6. **AI-native retrieval**: No CLI entry points. The AI describes what
     it needs, the DAG returns relevant capabilities.

Architecture:

    CapabilityNode          — Atomic unit: content + metadata + verification
        ↓ depends_on
    CapabilityEdge          — Typed dependency (requires, extends, composes, ...)
        ↓ organized by
    CapabilityDAG           — The graph: add, query, grow, merge, persist
        ↓ verified by
    VerificationOracle      — Pluggable per-domain verification
        ↓ grown by
    AutonomousGrowthEngine  — STP-generalized: mutate → verify → add
        ↓ shared via
    CommunitySync           — Cross-instance merge protocol

The DAG replaces / unifies:
  - FoundationBuilder's block system (lean_prover.py)
  - RAGRetrievalEngine's snippet index (rag_retrieval.py)
  - EvolutionEngine's genome tracking (evolution.py)
  - DynamicConstitution's knowledge base (dynamic_constitution.py)
  - SICA's improvement history (sica.py)
  - ConjectureEngine's proved library (lean_prover.py)

None of those are deleted — they continue to work. The DAG sits underneath
as the universal persistence + retrieval + growth layer. Existing modules
can read from and write to it via simple APIs.

References:
  - STP (ICML 2025): Self-play for autonomous theorem discovery
  - LeanAgent (ICLR 2025): Lifelong learning across repositories
  - FunSearch (Nature 2024): LLM-guided search for novel solutions
  - EUREKA (ICLR 2024): LLM reward design via evolutionary search
  - Voyager (NeurIPS 2023): Open-ended embodied agent skill library
"""

