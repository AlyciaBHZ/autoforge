@dataclass
class CapabilityNode:
    """Atomic unit of knowledge in the DAG.

    Content-addressed: the `id` is derived from (domain, content_hash),
    so identical capabilities across users/projects resolve to the same node.

    Fields:
      - id: Content-based hash (deterministic, dedup-friendly)
      - domain: Which knowledge domain this belongs to
      - content: The actual capability (Lean code, code snippet, design rule, ...)
      - summary: Human/AI-readable one-line description
      - tags: Free-form labels for retrieval
      - verification: How this was verified and confidence level
      - provenance: Where this came from (project, user, generation round)
      - usage_count: How many times this was retrieved and used
      - confidence: 0.0–1.0, grows with verification and usage
      - created_at: Timestamp
      - embedding: Optional semantic vector for retrieval (lazy-computed)
    """
    id: str
    domain: Domain
    content: str
    summary: str = ""
    tags: list[str] = field(default_factory=list)

    # Verification
    verification_type: VerificationType = VerificationType.UNVERIFIED
    confidence: float = 0.1
    verification_details: str = ""

    # Provenance
    source_project: str = ""
    source_user: str = ""
    generation_strategy: str = ""   # Which GrowthStrategy created this
    parent_ids: list[str] = field(default_factory=list)  # Nodes this was derived from

    # Usage tracking
    usage_count: int = 0
    success_count: int = 0          # Times it actually helped
    last_used: float = 0.0

    # Metadata
    created_at: float = field(default_factory=time.time)
    updated_at: float = field(default_factory=time.time)
    metadata: dict[str, Any] = field(default_factory=dict)

    # Embedding (lazy, not persisted in JSON by default)
    _embedding: list[float] | None = field(default=None, repr=False)

    def to_dict(self) -> dict[str, Any]:
        return {
            "id": self.id,
            "domain": self.domain.value,
            "content": self.content,
            "summary": self.summary,
            "tags": self.tags,
            "verification_type": self.verification_type.value,
            "confidence": self.confidence,
            "verification_details": self.verification_details,
            "source_project": self.source_project,
            "source_user": self.source_user,
            "generation_strategy": self.generation_strategy,
            "parent_ids": self.parent_ids,
            "usage_count": self.usage_count,
            "success_count": self.success_count,
            "last_used": self.last_used,
            "created_at": self.created_at,
            "updated_at": self.updated_at,
            "metadata": self.metadata,
        }

    @classmethod
    def from_dict(cls, data: dict[str, Any]) -> CapabilityNode:
        return cls(
            id=data["id"],
            domain=Domain(data.get("domain", "general")),
            content=data.get("content", ""),
            summary=data.get("summary", ""),
            tags=data.get("tags", []),
            verification_type=VerificationType(data.get("verification_type", "unverified")),
            confidence=data.get("confidence", 0.1),
            verification_details=data.get("verification_details", ""),
            source_project=data.get("source_project", ""),
            source_user=data.get("source_user", ""),
            generation_strategy=data.get("generation_strategy", ""),
            parent_ids=data.get("parent_ids", []),
            usage_count=data.get("usage_count", 0),
            success_count=data.get("success_count", 0),
            last_used=data.get("last_used", 0.0),
            created_at=data.get("created_at", time.time()),
            updated_at=data.get("updated_at", time.time()),
            metadata=data.get("metadata", {}),
        )


@dataclass
class CapabilityEdge:
    """Directed edge between two capability nodes."""
    source_id: str          # From node
    target_id: str          # To node
    edge_type: EdgeType
    weight: float = 1.0     # Strength of relationship
    metadata: dict[str, Any] = field(default_factory=dict)

    def to_dict(self) -> dict[str, Any]:
        return {
            "source_id": self.source_id,
            "target_id": self.target_id,
            "edge_type": self.edge_type.value,
            "weight": self.weight,
            "metadata": self.metadata,
        }

    @classmethod
    def from_dict(cls, data: dict[str, Any]) -> CapabilityEdge:
        return cls(
            source_id=data["source_id"],
            target_id=data["target_id"],
            edge_type=EdgeType(data.get("edge_type", "depends_on")),
            weight=data.get("weight", 1.0),
            metadata=data.get("metadata", {}),
        )


