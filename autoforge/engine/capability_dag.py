class DAGBridge:
    """Bridges existing AutoForge modules to the CapabilityDAG.

    Provides convenience methods so that existing modules (RAG, Evolution,
    SICA, LeanProver, etc.) can read/write to the DAG without knowing
    its internal structure.

    Usage by existing modules:
      - RAG: bridge.ingest_code_snippet(...)
      - Evolution: bridge.ingest_workflow_genome(...)
      - SICA: bridge.ingest_constitution_patch(...)
      - LeanProver: bridge.ingest_proof(...)
      - Any new domain: bridge.ingest(domain, content, ...)
    """

    def __init__(self, dag: CapabilityDAG) -> None:
        self._dag = dag

    # ── Generic ──

    def ingest(
        self,
        domain: Domain,
        content: str,
        summary: str = "",
        tags: list[str] | None = None,
        confidence: float = 0.2,
        **kwargs: Any,
    ) -> CapabilityNode:
        """Universal ingestion point."""
        return self._dag.add(
            content=content,
            domain=domain,
            summary=summary,
            tags=tags,
            confidence=confidence,
            **kwargs,
        )

    # ── Code Patterns (from RAG) ──

    def ingest_code_snippet(
        self,
        code: str,
        language: str,
        function_name: str = "",
        docstring: str = "",
        source_project: str = "",
        quality_score: float = 0.5,
    ) -> CapabilityNode:
        """Ingest a code snippet from RAG retrieval."""
        tags = [language]
        if function_name:
            tags.append(function_name)

        return self._dag.add(
            content=code,
            domain=Domain.CODE_PATTERN,
            summary=docstring or f"{language} function: {function_name}",
            tags=tags,
            confidence=quality_score * 0.8,
            verification_type=VerificationType.SELF_REPORT,
            source_project=source_project,
        )

    # ── Math Proofs (from LeanProver) ──

    def ingest_proof(
        self,
        lean_code: str,
        theorem_name: str = "",
        informal_statement: str = "",
        verified: bool = False,
        source_project: str = "",
    ) -> CapabilityNode:
        """Ingest a Lean 4 proof."""
        return self._dag.add(
            content=lean_code,
            domain=Domain.MATH_PROOF,
            summary=informal_statement or f"theorem {theorem_name}",
            tags=["lean4", "proof"] + ([theorem_name] if theorem_name else []),
            confidence=0.95 if verified else 0.3,
            verification_type=VerificationType.FORMAL if verified else VerificationType.LLM_EVAL,
            source_project=source_project,
        )

    # ── Workflow Patterns (from Evolution) ──

    def ingest_workflow(
        self,
        strategy_description: str,
        tech_fingerprint: str = "",
        fitness: float = 0.5,
        source_project: str = "",
    ) -> CapabilityNode:
        """Ingest a workflow genome / strategy."""
        tags = ["workflow"]
        if tech_fingerprint:
            tags.extend(tech_fingerprint.split("-"))

        return self._dag.add(
            content=strategy_description,
            domain=Domain.WORKFLOW,
            summary=f"Workflow: {strategy_description[:80]}",
            tags=tags,
            confidence=fitness,
            verification_type=VerificationType.TEST_SUITE,
            source_project=source_project,
        )

    # ── Constitution Rules (from SICA / DynamicConstitution) ──

    def ingest_constitution_patch(
        self,
        patch_content: str,
        target_role: str = "",
        source: str = "",
        priority: int = 5,
    ) -> CapabilityNode:
        """Ingest a constitution patch."""
        return self._dag.add(
            content=patch_content,
            domain=Domain.CONSTITUTION,
            summary=f"Constitution rule for {target_role}: {patch_content[:60]}",
            tags=["constitution", target_role, source],
            confidence=0.5,
            verification_type=VerificationType.SELF_REPORT,
            metadata={"priority": priority},
        )

    # ── Architecture Decisions ──

    def ingest_architecture_decision(
        self,
        decision: str,
        context: str = "",
        outcome_success: bool = True,
        source_project: str = "",
    ) -> CapabilityNode:
        """Ingest an architectural decision and its outcome."""
        return self._dag.add(
            content=f"{decision}\n\n## Context\n{context}" if context else decision,
            domain=Domain.ARCHITECTURE,
            summary=decision[:100],
            tags=["architecture"],
            confidence=0.7 if outcome_success else 0.2,
            verification_type=VerificationType.TEST_SUITE if outcome_success
                             else VerificationType.SELF_REPORT,
            source_project=source_project,
        )

    # ── Debugging Knowledge ──

    def ingest_debug_pattern(
        self,
        error_pattern: str,
        fix_strategy: str,
        success: bool = True,
        source_project: str = "",
    ) -> CapabilityNode:
        """Ingest a debugging pattern (error → fix mapping)."""
        content = f"## Error Pattern\n{error_pattern}\n\n## Fix\n{fix_strategy}"
        return self._dag.add(
            content=content,
            domain=Domain.DEBUGGING,
            summary=f"Fix for: {error_pattern[:60]}",
            tags=["debugging", "error-fix"],
            confidence=0.7 if success else 0.2,
            verification_type=VerificationType.TEST_SUITE if success
                             else VerificationType.SELF_REPORT,
            source_project=source_project,
        )

    # ── Query helpers ──

    def find_relevant(
        self,
        description: str,
        domain: Domain | None = None,
        top_k: int = 5,
    ) -> list[CapabilityNode]:
        """Find relevant capabilities for a given description.

        This is the main AI-facing API: describe what you need,
        get back relevant capabilities.
        """
        return self._dag.query(
            description,
            domain=domain,
            min_confidence=0.1,
            top_k=top_k,
        )

    def build_context(
        self,
        description: str,
        domain: Domain | None = None,
        max_tokens: int = 4000,
    ) -> str:
        """Build a context string from relevant capabilities.

        This is what gets injected into agent prompts — the AI doesn't
        need to know about the DAG structure, it just sees relevant
        knowledge formatted as context.
        """
        nodes = self.find_relevant(description, domain=domain, top_k=10)
        if not nodes:
            return ""

        parts = [f"## Relevant Capabilities ({len(nodes)} found)\n"]
        total_chars = 0

        for node in nodes:
            entry = (
                f"### [{node.domain.value}] {node.summary}\n"
                f"Confidence: {node.confidence:.2f} | "
                f"Used: {node.usage_count}× | "
                f"Tags: {', '.join(node.tags[:5])}\n"
                f"```\n{node.content[:1000]}\n```\n"
            )
            if total_chars + len(entry) > max_tokens * 4:  # ~4 chars per token
                break
            parts.append(entry)
            total_chars += len(entry)

        return "\n".join(parts)


