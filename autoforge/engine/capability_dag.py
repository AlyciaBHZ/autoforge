class Domain(str, Enum):
    """Knowledge domains — extensible, not exhaustive."""
    MATH_PROOF = "math_proof"           # Lean theorems, proof strategies
    CODE_PATTERN = "code_pattern"       # Reusable code templates, idioms
    ARCHITECTURE = "architecture"       # System design decisions
    UI_DESIGN = "ui_design"             # Visual/UX conventions
    WORKFLOW = "workflow"               # Build/deploy/test strategies
    DEBUGGING = "debugging"             # Bug patterns + fix strategies
    CONSTITUTION = "constitution"       # Agent behavior rules
    DOMAIN_KNOWLEDGE = "domain_knowledge"  # Subject-matter expertise
    GENERAL = "general"                 # Catch-all


class VerificationType(str, Enum):
    """How a capability was verified."""
    FORMAL = "formal"           # Lean type-checker, Z3, etc. — 100% certain
    TEST_SUITE = "test_suite"   # Automated tests passed — high confidence
    LLM_EVAL = "llm_eval"      # LLM evaluated correctness — medium confidence
    COMMUNITY = "community"     # Multiple users confirmed — social proof
    SELF_REPORT = "self_report" # System self-assessed — low confidence
    UNVERIFIED = "unverified"   # Not yet verified


class EdgeType(str, Enum):
    """Relationship between capabilities."""
    DEPENDS_ON = "depends_on"     # B requires A (A → B)
    EXTENDS = "extends"           # B generalizes or strengthens A
    COMPOSES = "composes"         # C = A + B combined
    CONTRADICTS = "contradicts"   # A and B are mutually exclusive
    SUPERSEDES = "supersedes"     # B makes A obsolete
    ANALOGOUS = "analogous"       # A in domain X ≈ B in domain Y
    DERIVED_FROM = "derived_from" # B was generated by mutating A


class GrowthStrategy(str, Enum):
    """How new nodes are generated."""
    GENERALIZE = "generalize"         # Weaken preconditions
    SPECIALIZE = "specialize"         # Strengthen for specific case
    COMPOSE = "compose"               # Combine two existing nodes
    ANALOGIZE = "analogize"           # Transfer across domains
    CONTRAPOSITIVE = "contrapositive" # Logical negation / inversion
    DECOMPOSE = "decompose"           # Break into sub-capabilities
    BOUNDARY_TEST = "boundary_test"   # Test edge cases / limits
    ABSTRACT = "abstract"             # Extract common pattern


